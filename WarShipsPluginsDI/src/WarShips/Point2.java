package WarShips;

/**
 *  Simple two dimensional point
 */
public class Point2 {

    public Point2(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    private int x;
    private int y;
}
